const getDates = (x, dif) => {
  const array = [];
  let i;
  for (i = 0; i < dif + 30; i++) {
    let acumSub = subSeconds(x, i);
    getSeconds(acumSub) === 0 &&
      array.push({
        asd: 1,
        id: getUnixTime(acumSub),
        content: '',
        start_ts: getUnixTime(acumSub),
        start: acumSub,
        end: addSeconds(acumSub, 60),
        style: 'color: red; background-color: pink;cursor: pointer',
        type: 'background',
        content: '',
        title: 'Record',
        editable: { updateTime: true },
      });
  }

  return reverse(array);
};

const formatEvents = (x) => {
  const array = [];

  x.map((i) => {
    let varDate = fromUnixTime(i.start_ts);
    let varDateGetSeconds = getSeconds(varDate);

    varDateGetSeconds >= 0 && varDateGetSeconds < 30
      ? array.push({
          ...i,
          start_ts: varDate.setSeconds(0) / 1000,
          to_play: i.start_ts,
        })
      : array.push({
          ...i,
          start_ts: varDate.setSeconds(30) / 1000,
          to_play: i.start_ts,
        });
  });

  return array;
};

