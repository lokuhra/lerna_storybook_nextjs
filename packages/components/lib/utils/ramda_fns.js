const diffNowToDate = (timestamp) => {
  let d = new Date(timestamp * 1000);
  let result = differenceInMilliseconds(Date.now(), d);
  let minutes = Math.floor(result / 60000);
  let seconds = ((result % 60000) / 1000).toFixed(0);
  return `Hace ${minutes}m y ${seconds < 10 ? '' : ''}${seconds}s`;
};

